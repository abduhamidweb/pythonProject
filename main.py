# O'zgaruvchilar orqali sonlarni olish
son1 = float(input("Birinchi sonni kiriting: "))
son2 = float(input("Ikkinchi sonni kiriting: "))

# Matematik amallar
yigindi = son1 + son2
ayirma = son1 - son2
kopaytma = son1 * son2
bolinma = son1 / son2
ildiz = son1 ** son2

# Natijalarni chiqarish
print("Yig'indi: ", yigindi)
print("Ayirma: ", ayirma)
print("Kopaytma: ", kopaytma)
print("Bo'linma: ", bolinma)
print("Ildiz: ", ildiz)
